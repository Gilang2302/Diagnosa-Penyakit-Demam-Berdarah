<br/>
Demam berdarah adalah penyakit yang cukup umum terjadi di daerah tropis dan subtropis. Penyakit ini sering muncul pada musim hujan ketika populasi nyamuk Aedes aegypti, vektor utama penyebar demam berdarah, meningkat. Demam berdarah dapat menyebabkan komplikasi serius jika tidak segera ditangani.
